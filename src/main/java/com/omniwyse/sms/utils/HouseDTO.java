package com.omniwyse.sms.utils;

public class HouseDTO {
	private long gradeid;
	private String gradename;
	private String syllabustype;
	private long noofstudents;
	private long totalnoofstudents;
	public long getGradeid() {
		return gradeid;
	}
	public void setGradeid(long gradeid) {
		this.gradeid = gradeid;
	}
	public String getGradename() {
		return gradename;
	}
	public void setGradename(String gradename) {
		this.gradename = gradename;
	}
	public String getSyllabustype() {
		return syllabustype;
	}
	public void setSyllabustype(String syllabustype) {
		this.syllabustype = syllabustype;
	}
	public long getNoofstudents() {
		return noofstudents;
	}
	public void setNoofstudents(long noofstudents) {
		this.noofstudents = noofstudents;
	}
	public long getTotalnoofstudents() {
		return totalnoofstudents;
	}
	public void setTotalnoofstudents(long totalnoofstudents) {
		this.totalnoofstudents = totalnoofstudents;
	}
	
	

}
